#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	int		i;
	int		k;

	i = 0;
	k = 0;
	while (dest[k] != '\0')
		k++;
	while (src[i] != '\0')
	{
		dest[k] = src[i];
		i++, k++;
	}
	dest[k] = '\0';
	return (dest);
}
